CREATE TABLE ClientType (
	client_type_id INT PRIMARY KEY,
	client_type_name VARCHAR(50) NOT NULL,
	client_type_card_amount INT NOT NULL,
	client_type_withdraw_limit INT NOT NULL,
	client_type_fee FLOAT NOT NULL
);

CREATE TABLE AccountType (
	account_type_id INT PRIMARY KEY,
	account_type_name VARCHAR(50) NOT NULL,
	account_type_currency VARCHAR(10) NOT NULL
);

CREATE TABLE CardBrand (
	card_brand_id INT PRIMARY KEY,
	card_brand_name VARCHAR(50) NOT NULL
);

CREATE TABLE Address (
	address_id INT PRIMARY KEY,
	address_name VARCHAR(45) NOT NULL
);

CREATE TABLE Branch (
	branch_id INT PRIMARY KEY,
	branch_name VARCHAR(50) NOT NULL,
	branch_address VARCHAR(255) NOT NULL,
	fk_branch_address_id INT NOT NULL,
	FOREIGN KEY (fk_branch_address_id) REFERENCES Address(address_id)
);
	
CREATE TABLE Customer (
	customer_id INT PRIMARY KEY,
	customer_DNI VARCHAR(10) NOT NULL,
	customer_name VARCHAR(50) NOT NULL,
	customer_surname VARCHAR(50) NOT NULL,
	customer_birth DATE NOT NULL,
	fk_customer_branch_id INT NOT NULL,
	fk_customer_address_id INT NOT NULL,
	fk_customer_client_type_id INT NOT NULL,
	FOREIGN KEY (fk_customer_branch_id) REFERENCES Branch(branch_id),
	FOREIGN KEY (fk_customer_address_id) REFERENCES Address(address_id),
	FOREIGN KEY (fk_customer_client_type_id) REFERENCES ClientType(client_type_id)
);

CREATE TABLE Employee (
	employee_id INT PRIMARY KEY,
	employee_name VARCHAR(50) NOT NULL,
	employee_surname VARCHAR(50) NOT NULL,
	employee_hire_date DATE NOT NULL,
	employee_DNI VARCHAR(10) NOT NULL,
	fk_employee_branch_id INT NOT NULL,
	fk_employee_address_id INT NOT NULL,
	FOREIGN KEY (fk_employee_branch_id) REFERENCES Branch(branch_id),
	FOREIGN KEY (fk_employee_address_id) REFERENCES Address(address_id)
);
	
CREATE TABLE CustomerAccount (
	customer_account_id INT PRIMARY KEY,
	customer_account_balance INT NOT NULL,
	fk_customer_account_customer_id INT NOT NULL,
	fk_customer_account_account_type_id INT NOT NULL,
	FOREIGN KEY (fk_customer_account_customer_id) REFERENCES Customer(customer_id),
	FOREIGN KEY (fk_customer_account_account_type_id) REFERENCES AccountType(account_type_id)
);

CREATE TABLE Loan (
	loan_id INT PRIMARY KEY,
	loan_payment VARCHAR(50) NOT NULL,
	loan_date DATE NOT NULL,
	loan_total INT NOT NULL,
	fk_loan_customer_id INT NOT NULL,
	FOREIGN KEY (fk_loan_customer_id) REFERENCES Customer(customer_id)
);

CREATE TABLE Card (
	card_id INT PRIMARY KEY,
	card_number VARCHAR(20) NOT NULL,
	card_cvv INT NOT NULL,
	card_grand_date DATE, -- Fecha de otorgamiento
	card_expire_date DATE,
	card_type VARCHAR(25), /*debito o credito*/
	fk_card_brand_id INT NOT NULL,
	fk_card_customer_id INT NOT NULL,
	FOREIGN KEY (fk_card_brand_id) REFERENCES CardBrand(card_brand_id) ,
	FOREIGN KEY (fk_card_customer_id) REFERENCES Customer(customer_id)
);

